# HORIZON-AWARE FEATURE SELECTION
# ============================================================

# Long-horizon rolling windows used by long_horizon_and_feature_engineering.py.
_LONG_WINDOWS = (30, 60, 90)
_LONG_LAGS = (5, 10, 21)

# Cyclic calendar features and regime-aware features kept across ALL horizons.
_ALWAYS_KEEP = (
    "month_sin", "month_cos", "dow_sin", "dow_cos",
    "drawdown_from_252d_high", "vol_60d_pct_rank", "return_30d_pct_rank",
)


def get_horizon_feature_list(all_features, horizon):
    """
    Filters the feature list based on trading horizon.

    Day   -> current values + lag1 only
             (drops *_lag2, *_lag5, *_lag10, *_lag21, all rolling aggregates)
    Swing -> current + lag1 + lag2 + lag5 + 30-day rolling aggregates
             (drops *_lag10, *_lag21, 60- and 90-day rolling aggregates)
    Long  -> all features (no filtering)

    Cyclic calendar features and regime-aware features are kept in all
    horizons regardless of the suffix-based filtering rules.
    """
    horizon = str(horizon).lower()
    if horizon not in ("day", "swing", "long"):
        raise ValueError(
            f"Unknown horizon: '{horizon}'. Expected 'day', 'swing', or 'long'."
        )

    if horizon == "long":
        return list(all_features)

    if horizon == "day":
        excluded_lag_suffixes = [f"_lag{k}" for k in (2,) + _LONG_LAGS]
        excluded_window_suffixes = (
            [f"_cum_{w}" for w in _LONG_WINDOWS]
            + [f"_std_{w}" for w in _LONG_WINDOWS]
            + [f"_mean_{w}" for w in _LONG_WINDOWS]
        )
    else:  # swing
        excluded_lag_suffixes = [f"_lag{k}" for k in (10, 21)]
        excluded_window_suffixes = (
            [f"_cum_{w}" for w in (60, 90)]
            + [f"_std_{w}" for w in (60, 90)]
            + [f"_mean_{w}" for w in (60, 90)]
        )

    excluded = tuple(excluded_lag_suffixes + excluded_window_suffixes)
    return [
        col for col in all_features
        if (not col.endswith(excluded)) or (col in _ALWAYS_KEEP)
    ]


