# PER-TICKER MINIMUM HISTORY FILTER
# ============================================================

def filter_min_ticker_history(df, min_ticker_history=500):
    """
    Removes Tickers with fewer than `min_ticker_history` rows. Keeps only
    assets that can produce stable rolling statistics and contribute
    meaningfully to all four chronological folds.
    """
    if min_ticker_history is None or min_ticker_history <= 0:
        return df, []

    ticker_counts = df.groupby("Ticker").size()
    keep_tickers = ticker_counts[ticker_counts >= min_ticker_history].index.tolist()
    drop_tickers = ticker_counts[ticker_counts < min_ticker_history].index.tolist()

    print(
        f"\nMin-history filter (>= {min_ticker_history} rows):"
        f" keeping {len(keep_tickers)} ticker(s), dropping {len(drop_tickers)}."
    )
    if drop_tickers:
        for t in drop_tickers:
            print(f"   - dropped: {t} ({int(ticker_counts[t])} rows)")

    df_out = df[df["Ticker"].isin(keep_tickers)].reset_index(drop=True)
    print(f"   Final row count: {len(df_out)} (was {len(df)})")
    return df_out, drop_tickers


