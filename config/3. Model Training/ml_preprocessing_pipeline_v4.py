# ============================================================

def prepare_tabular_data(
    df,
    target_col,
    train_ratio=0.70,
    val_ratio=0.15,
    test_ratio=0.15,
    scale_for_model=False,
    scaler_type="minmax",
    purge_gap_days=0,
    inner_purge_days=None,
    apply_subsampling=False,
    subsampling_ratio=0.25,
    apply_winsor=True,
    winsor_lower_pct=0.01,
    winsor_upper_pct=0.99,
    horizon=None,
    min_ticker_history=500,
    random_state=42,
):
    """
    Main preprocessing for tabular ML models.

    Pipeline order:
        1. Validate input dataframe.
        2. Per-ticker minimum history filter.
        3. Four-way chronological split (train / val_select / val_calib / test).
        4. Horizon-aware feature filtering (optional).
        5. Train-only winsorization (optional).
        6. Train-only scaling (optional, for linear models).
        7. Train-only temporal negative subsampling (optional).

    Returns
    -------
    X_train, y_train,
    X_val_select, y_val_select,
    X_val_calib, y_val_calib,
    X_test, y_test,
    metadata
    """
    df = validate_input_dataframe(df, target_col)

    df, dropped_tickers = filter_min_ticker_history(df, min_ticker_history=min_ticker_history)

    train_df, vs_df, vc_df, test_df, split_info = chronological_split_4way(
        df,
        train_ratio=train_ratio,
        val_ratio=val_ratio,
        test_ratio=test_ratio,
        purge_gap_days=purge_gap_days,
        inner_purge_days=inner_purge_days,
    )

    y_train = train_df[target_col].copy()
    y_val_select = vs_df[target_col].copy()
    y_val_calib = vc_df[target_col].copy()
    y_test = test_df[target_col].copy()

    all_feature_cols = get_feature_columns(
        df, target_col=target_col, drop_date=True, drop_ticker=True, drop_all_targets=True,
    )

    if horizon is not None:
        feature_cols = get_horizon_feature_list(all_feature_cols, horizon)
        print(
            f"\nHorizon-aware feature selection: horizon='{horizon}' -> "
            f"{len(feature_cols)}/{len(all_feature_cols)} features kept."
        )
    else:
        feature_cols = all_feature_cols

    X_train = train_df[feature_cols].copy()
    X_val_select = vs_df[feature_cols].copy()
    X_val_calib = vc_df[feature_cols].copy()
    X_test = test_df[feature_cols].copy()

    # NaN sanity checks.
    for name, X in (
        ("train", X_train), ("val_select", X_val_select),
        ("val_calib", X_val_calib), ("test", X_test),
    ):
        if X.isna().any().any():
            raise ValueError(f"NaN values detected in {name} features.")
    for name, y in (
        ("train", y_train), ("val_select", y_val_select),
        ("val_calib", y_val_calib), ("test", y_test),
    ):
        if y.isna().any():
            raise ValueError(f"NaN values detected in {name} target.")

    # Train-only winsorization.
    winsor_bounds = {}
    if apply_winsor:
        winsor_bounds = compute_winsor_bounds(
            X_train, lower_pct=winsor_lower_pct, upper_pct=winsor_upper_pct,
        )
        print(
            f"\nWinsorization at {winsor_lower_pct:.2%}/{winsor_upper_pct:.2%} "
            f"on {len(winsor_bounds)} features (train-only percentiles)."
        )
        X_train = apply_winsorization(X_train, winsor_bounds)
        X_val_select = apply_winsorization(X_val_select, winsor_bounds)
        X_val_calib = apply_winsorization(X_val_calib, winsor_bounds)
        X_test = apply_winsorization(X_test, winsor_bounds)

    # Train-only scaling.
    scaler = None
    if scale_for_model:
        X_train, X_val_select, X_val_calib, X_test, scaler = scale_features(
            X_train, X_val_select, X_val_calib, X_test, scaler_type=scaler_type,
        )

    # Train-only subsampling.
    if apply_subsampling:
        X_train, y_train = apply_temporal_negative_subsampling(
            X_train, y_train,
            target_positive_ratio=subsampling_ratio,
            random_state=random_state,
        )

    print_class_distribution(y_train, label="Train")
    print_class_distribution(y_val_select, label="Val_Select")
    print_class_distribution(y_val_calib, label="Val_Calib")
    print_class_distribution(y_test, label="Test")

    pos_weight = compute_positive_class_weight(y_train)

    metadata = {
        "split_info": split_info,
        "feature_columns": feature_cols,
        "all_feature_columns": all_feature_cols,
        "target_column": target_col,
        "horizon": horizon,
        "scaler": scaler,
        "winsor_bounds": winsor_bounds,
        "winsor_applied": apply_winsor,
        "recommended_positive_class_weight": pos_weight,
        "apply_subsampling": apply_subsampling,
        "subsampling_ratio": subsampling_ratio if apply_subsampling else None,
        "purge_gap_days": purge_gap_days,
        "inner_purge_days": split_info["inner_purge_days"],
        "min_ticker_history": min_ticker_history,
        "dropped_tickers": dropped_tickers,
    }

    return (
        X_train, y_train,
        X_val_select, y_val_select,
        X_val_calib, y_val_calib,
        X_test, y_test,
        metadata,
    )


