# WINSORIZATION (TRAIN-ONLY PERCENTILES)
# ============================================================

# Features bounded by construction; no winsorization needed.
_WINSOR_SKIP = frozenset({
    "month_sin", "month_cos", "dow_sin", "dow_cos",
    "drawdown_from_252d_high", "vol_60d_pct_rank", "return_30d_pct_rank",
})


def compute_winsor_bounds(X_train, lower_pct=0.01, upper_pct=0.99, skip_features=None):
    """
    Computes per-feature 1%/99% winsorization bounds from the training set.
    Bounded features are skipped automatically.
    """
    if skip_features is None:
        skip_features = _WINSOR_SKIP
    bounds = {}
    for col in X_train.columns:
        if col in skip_features:
            continue
        lo = float(X_train[col].quantile(lower_pct))
        hi = float(X_train[col].quantile(upper_pct))
        if not np.isfinite(lo) or not np.isfinite(hi) or lo >= hi:
            continue
        bounds[col] = (lo, hi)
    return bounds


def apply_winsorization(X, bounds):
    """Clips each feature in `X` to the (lo, hi) bounds learned on TRAIN."""
    X_out = X.copy()
    for col, (lo, hi) in bounds.items():
        if col in X_out.columns:
            X_out[col] = X_out[col].clip(lower=lo, upper=hi)
    return X_out


