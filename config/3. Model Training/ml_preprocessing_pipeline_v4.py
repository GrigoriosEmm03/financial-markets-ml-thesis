def validate_input_dataframe(df, target_col):
    required_cols = ["Date", "Ticker", target_col]
    missing = [col for col in required_cols if col not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns: {missing}")
    if df.empty:
        raise ValueError("Input DataFrame is empty.")

    df = df.copy()
    df["Date"] = pd.to_datetime(df["Date"], errors="coerce")
    if df["Date"].isna().any():
        raise ValueError("Some values in 'Date' could not be converted to datetime.")

    df = df.sort_values(["Date", "Ticker"]).reset_index(drop=True)

    if df[target_col].isna().all():
        raise ValueError(f"Target column '{target_col}' contains only NaN values.")

    return df


def get_target_columns(df):
    return [col for col in df.columns if col.startswith("Target_")]


def get_feature_columns(df, target_col, drop_date=True, drop_ticker=True, drop_all_targets=True):
    cols_to_drop = []
    if drop_date:
        cols_to_drop.append("Date")
    if drop_ticker:
        cols_to_drop.append("Ticker")
    if drop_all_targets:
        cols_to_drop.extend(get_target_columns(df))
    else:
        cols_to_drop.append(target_col)
    cols_to_drop = [col for col in cols_to_drop if col in df.columns]
    return [col for col in df.columns if col not in cols_to_drop]


def print_class_distribution(y, label="Dataset"):
    counts = pd.Series(y).value_counts(dropna=False).sort_index()
    proportions = pd.Series(y).value_counts(normalize=True, dropna=False).sort_index()
    print(f"\nClass distribution - {label}")
    print("Counts:")
    print(counts.to_dict())
    print("Proportions:")
    print({k: round(v, 4) for k, v in proportions.to_dict().items()})


def compute_positive_class_weight(y):
    y_series = pd.Series(y).dropna()
    unique_vals = sorted(y_series.unique())
    if set(unique_vals) != {0, 1}:
        print("[WARNING] Positive class weight not computed (target not binary {0,1}).")
        return None
    negatives = (y_series == 0).sum()
    positives = (y_series == 1).sum()
    if positives == 0:
        print("[WARNING] No positive cases found.")
        return None
    weight = negatives / positives
    print(f"\nSuggested positive class weight: {weight:.4f}")
    return weight


