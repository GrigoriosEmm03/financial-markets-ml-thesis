# CLASS IMBALANCE MITIGATION (TIME-SERIES SAFE)
# ============================================================

def apply_temporal_negative_subsampling(
    X_train, y_train, target_positive_ratio=0.25, random_state=42,
):
    """
    Time-series-safe class imbalance mitigation via majority undersampling.
    Keeps ALL positive samples and randomly subsamples the negatives to
    reach the desired positive rate. Apply ONLY to the training set.
    """
    if target_positive_ratio <= 0 or target_positive_ratio >= 1:
        raise ValueError("target_positive_ratio must be in the open interval (0, 1).")

    X_train = X_train.reset_index(drop=True)
    y_train = pd.Series(y_train).reset_index(drop=True)

    pos_idx = y_train.index[y_train == 1].to_numpy()
    neg_idx = y_train.index[y_train == 0].to_numpy()
    n_pos, n_neg = len(pos_idx), len(neg_idx)

    print("\nApplying temporal negative subsampling (train only)")
    print(f"   Original train size:    {len(y_train)}")
    print(f"   Positives:              {n_pos}")
    print(f"   Negatives:              {n_neg}")
    print(f"   Target positive ratio:  {target_positive_ratio:.2f}")

    if n_pos == 0:
        print("   [WARNING] No positives available. Subsampling skipped.")
        return X_train, y_train

    n_neg_target = int(round(n_pos * (1.0 - target_positive_ratio) / target_positive_ratio))
    if n_neg_target >= n_neg:
        print(f"   Current negatives ({n_neg}) already <= target ({n_neg_target}). Skipped.")
        return X_train, y_train

    rng = np.random.default_rng(random_state)
    sampled_neg = rng.choice(neg_idx, size=n_neg_target, replace=False)
    combined_idx = np.sort(np.concatenate([pos_idx, sampled_neg]))

    X_out = X_train.loc[combined_idx].reset_index(drop=True)
    y_out = y_train.loc[combined_idx].reset_index(drop=True)

    new_pos_rate = float(y_out.mean())
    print(f"   Negatives kept:         {n_neg_target}")
    print(f"   New train size:         {len(y_out)}")
    print(f"   New positive rate:      {new_pos_rate:.4f}")
    return X_out, y_out


