def scale_features(X_train, X_val_select, X_val_calib, X_test, scaler_type="minmax"):
    """Train-only fit; transform applied to all folds."""
    print(f"\nScaling features ({scaler_type}, train-only fit)...")
    if scaler_type.lower() == "minmax":
        scaler = MinMaxScaler()
    else:
        raise ValueError("Currently supported scaler_type: 'minmax'")

    def _to_df(arr, src):
        return pd.DataFrame(arr, columns=src.columns, index=src.index)

    X_tr = _to_df(scaler.fit_transform(X_train), X_train)
    X_vs = _to_df(scaler.transform(X_val_select), X_val_select)
    X_vc = _to_df(scaler.transform(X_val_calib), X_val_calib)
    X_te = _to_df(scaler.transform(X_test), X_test)
    return X_tr, X_vs, X_vc, X_te, scaler


