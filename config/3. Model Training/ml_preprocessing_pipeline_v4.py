# OPTIONAL: DUMP SPLIT CSVS FOR DEBUGGING
# ============================================================

def dump_splits_to_csv(
    X_train, y_train,
    X_val_select, y_val_select,
    X_val_calib, y_val_calib,
    X_test, y_test,
    output_dir, prefix,
):
    """
    Saves each split (X + y) as a separate CSV under `output_dir`. Useful
    for offline inspection of the actual rows used in training.
    """
    os.makedirs(output_dir, exist_ok=True)
    target_name = y_train.name if y_train.name is not None else "target"

    splits = [
        ("train", X_train, y_train),
        ("val_select", X_val_select, y_val_select),
        ("val_calib", X_val_calib, y_val_calib),
        ("test", X_test, y_test),
    ]
    print(f"\nDumping splits to: {output_dir}")
    for fold_name, X_fold, y_fold in splits:
        df_fold = X_fold.copy()
        df_fold[target_name] = y_fold.values
        path = os.path.join(output_dir, f"{prefix}_{fold_name}.csv")
        df_fold.to_csv(path, index=False)
        print(f"   Saved split: {path} ({len(df_fold)} rows)")


