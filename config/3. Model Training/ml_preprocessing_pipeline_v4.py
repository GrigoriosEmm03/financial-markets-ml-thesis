"""
ml_preprocessing_pipeline_v4.py

Preprocessing pipeline for the thesis ML training scripts (v4).

Changes vs v3:
    1. DATASET_PATHS now point to the v3 ML-ready CSVs (output of
       long_horizon_and_feature_engineering.py).
    2. New: per-ticker minimum history filter (default 500 rows).
       Tickers with fewer rows are dropped before any split.
    3. New: train-only winsorization at 1% / 99% percentiles.
       Bounds saved in metadata['winsor_bounds'] so the same clip
       can be applied at live inference time. Bounded features
       (cyclic encodings, percentile ranks, drawdown) are skipped
       automatically.
    4. New: 4-way chronological split with purge gaps:
            train -> val_select -> val_calib -> test
       val_select is used for model selection and threshold reporting,
       val_calib is held out and used only for isotonic calibration.
       This breaks the optimism bias of using a single val fold for
       both selection and calibration that we observed in v3.
    5. prepare_tabular_data now returns 9 objects:
            X_train, y_train,
            X_val_select, y_val_select,
            X_val_calib, y_val_calib,
            X_test, y_test,
            metadata
    6. New: optional dump of split CSVs to a configurable folder
       (DEFAULT_OUTPUT_DIR) for offline inspection.
    7. get_horizon_feature_list updated for the new feature set.
       Cyclic calendar features and regime-aware features are kept
       across all horizons.

Environment: thesis_env (Anaconda), Python 3.x
"""

