# 4-WAY CHRONOLOGICAL SPLIT (train / val_select / val_calib / test)
# ============================================================

def chronological_split_4way(
    df,
    train_ratio=0.70,
    val_ratio=0.15,
    test_ratio=0.15,
    purge_gap_days=0,
    inner_purge_days=None,
):
    """
    Chronological 4-way split:
        train -> [purge] -> val_select -> [inner_purge] -> val_calib -> [purge] -> test

    val_select is used for candidate scoring and threshold reporting.
    val_calib is held out and used only for probability calibration.

    Parameters
    ----------
    df : pd.DataFrame
    train_ratio, val_ratio, test_ratio : floats summing to 1.0.
        val_ratio is split internally into val_select (first half)
        and val_calib (second half).
    purge_gap_days : int
        Embargo (in calendar days) between train/val and val/test.
        Aligned with the look-forward of the target.
    inner_purge_days : int or None
        Embargo between val_select and val_calib. If None, defaults
        to purge_gap_days // 2.
    """
    if round(train_ratio + val_ratio + test_ratio, 5) != 1.0:
        raise ValueError("train_ratio + val_ratio + test_ratio must sum to 1.0")
    if purge_gap_days < 0:
        raise ValueError("purge_gap_days must be >= 0")
    if inner_purge_days is None:
        inner_purge_days = max(0, purge_gap_days // 2)

    df = df.copy()
    df["Date"] = pd.to_datetime(df["Date"])
    df = df.sort_values(["Date", "Ticker"]).reset_index(drop=True)

    unique_dates = np.array(sorted(df["Date"].unique()))
    total_dates = len(unique_dates)
    if total_dates < 12:
        raise ValueError("Not enough unique dates to perform a reliable 4-way split.")

    train_end_idx = max(1, int(total_dates * train_ratio) - 1)
    val_end_idx = max(train_end_idx + 1, int(total_dates * (train_ratio + val_ratio)) - 1)
    if val_end_idx >= total_dates:
        val_end_idx = total_dates - 2

    val_mid_idx = train_end_idx + max(1, (val_end_idx - train_end_idx) // 2)

    train_end_date = pd.Timestamp(unique_dates[train_end_idx])
    val_mid_date = pd.Timestamp(unique_dates[val_mid_idx])
    val_end_date = pd.Timestamp(unique_dates[val_end_idx])

    outer_gap = pd.Timedelta(days=purge_gap_days)
    inner_gap = pd.Timedelta(days=inner_purge_days)

    val_select_start_cut = train_end_date + outer_gap
    val_calib_start_cut = val_mid_date + inner_gap
    test_start_cut = val_end_date + outer_gap

    train_df = df[df["Date"] <= train_end_date].copy()
    val_select_df = df[
        (df["Date"] > val_select_start_cut) & (df["Date"] <= val_mid_date)
    ].copy()
    val_calib_df = df[
        (df["Date"] > val_calib_start_cut) & (df["Date"] <= val_end_date)
    ].copy()
    test_df = df[df["Date"] > test_start_cut].copy()

    # Sanity checks.
    for name, part in (
        ("train", train_df), ("val_select", val_select_df),
        ("val_calib", val_calib_df), ("test", test_df),
    ):
        if part.empty:
            raise ValueError(
                f"Split '{name}' is empty. Check dataset size, ratios, and gaps."
            )
    if train_df["Date"].max() >= val_select_df["Date"].min():
        raise ValueError("Date overlap between train and val_select.")
    if val_select_df["Date"].max() >= val_calib_df["Date"].min():
        raise ValueError("Date overlap between val_select and val_calib.")
    if val_calib_df["Date"].max() >= test_df["Date"].min():
        raise ValueError("Date overlap between val_calib and test.")

    split_info = {
        "train_end_date": train_end_date,
        "val_mid_date": val_mid_date,
        "val_end_date": val_end_date,
        "purge_gap_days": purge_gap_days,
        "inner_purge_days": inner_purge_days,
        "train_rows": len(train_df),
        "val_select_rows": len(val_select_df),
        "val_calib_rows": len(val_calib_df),
        "test_rows": len(test_df),
        "train_unique_dates": train_df["Date"].nunique(),
        "val_select_unique_dates": val_select_df["Date"].nunique(),
        "val_calib_unique_dates": val_calib_df["Date"].nunique(),
        "test_unique_dates": test_df["Date"].nunique(),
    }

    print("\n4-way Chronological Split Completed")
    print(
        f"   train: <= {train_end_date.strftime('%Y-%m-%d')} | "
        f"val_sel: <= {val_mid_date.strftime('%Y-%m-%d')} | "
        f"val_cal: <= {val_end_date.strftime('%Y-%m-%d')} | "
        f"test: > {val_end_date.strftime('%Y-%m-%d')} (+{purge_gap_days}d gap)"
    )
    print(
        f"   Rows -> Train: {split_info['train_rows']} | "
        f"Val_Sel: {split_info['val_select_rows']} | "
        f"Val_Cal: {split_info['val_calib_rows']} | "
        f"Test: {split_info['test_rows']}"
    )

    return train_df, val_select_df, val_calib_df, test_df, split_info


